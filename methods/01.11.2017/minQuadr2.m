function  a = minQuadr2(x, y)
    n = length(x);
    c(1,1) = n;
    c(1,2) = 0;
    c(1,3) = 0;
    c(2,1) = 0;
    c(2,2) = 0;
    c(2,3) = 0;
    c(3,1) = 0;
    c(3,2) = 0;
    c(3,3) = 0;
    
    b(1) = 0;
    b(2) = 0;
    b(3) = 0;
    
    for i = 1 : n
        c(1,2) = c(1,2) + x(i);
        c(1,3) = c(1,3) + x(i)^2;
        c(2,3) = c(2,3) + x(i)^3;
        c(3,3) = c(3,3) + x(i)^4;
        
        b(1) = b(1) + y(i);
        b(2) = b(2) + x(i) * y(i);
        b(3) = b(3) + x(i)^2 * y(i);
    end
    c(2,1) = c(1,2);
    c(2,2) = c(1,3);
    c(3,1) = c(1,3);
    c(3,2) = c(2,3);
    b = b';
    a = c^(-1) * b;
end